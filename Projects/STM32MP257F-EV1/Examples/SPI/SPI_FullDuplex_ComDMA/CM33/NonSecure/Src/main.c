{
	/* HPDMA3 clock enable */
	if(ResMgr_Request(RESMGR_RESOURCE_RIF_RCC, RESMGR_RCC_RESOURCE(85)) == RESMGR_STATUS_ACCESS_OK)
	{
		__HAL_RCC_HPDMA3_CLK_ENABLE();
	}
}

