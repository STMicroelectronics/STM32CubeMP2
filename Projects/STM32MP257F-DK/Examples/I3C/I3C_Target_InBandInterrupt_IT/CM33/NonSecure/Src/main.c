/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
#ifdef DEBUG
  while(debug);
#endif /* DEBUG */
  /* USER CODE BEGIN 1 */
  /* STM32MP2xx HAL library initialization:
       - Systick timer is configured by default as source of time base, but user
             can eventually implement his proper time base source (a general purpose
             timer for example or other time source), keeping in mind that Time base
             duration should be kept 1ms since PPP_TIMEOUT_VALUEs are defined and
             handled in milliseconds basis.
       - Set NVIC Group Priority to 4
       - Low Level Initialization
     */
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  if (IS_DEVELOPER_BOOT_MODE())
  {
	  SystemClock_Config();
  }

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Configure LED3 */
  BSP_LED_Init(LED3);

  if(!IS_DEVELOPER_BOOT_MODE())
  {
    /* IPCC initialization */
    MX_IPCC_Init();
  
    /*Corpo Sync Initialization*/
    CoproSync_Init();
  }

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I3C4_Init();

  /* USER CODE BEGIN 2 */
  /* Configure USER push-button */
  BSP_PB_Init(BUTTON_USER2, BUTTON_MODE_GPIO);

  /*##- Start the listen mode process ####################################*/
  /* Activate notifications for specially for this example
     - Dynamic address association
     - In Band Interrupt Ended process
 */
  if (HAL_I3C_ActivateNotification(&hi3c4, NULL, (HAL_I3C_IT_DAUPDIE | EVENT_ID_IBIEND)) != HAL_OK)
  {
    /* Error_Handler() function is called when error occurs. */
    Error_Handler();
  }

  /*##- Wait for the end of the transfer #################################*/
  /*  Before starting a new communication transfer, you need to check the
  end of current transfer before starting a new one.
  For simplicity reasons, this example is just waiting till the end of the
  transfer, but application may perform other tasks while transfer operation
  is ongoing. */
  while (ubDynamicAddressCplt != 1)
  {
  }
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
	/* Wait for USER push-button press before starting the Communication */
	while (BSP_PB_GetState(BUTTON_USER2) != GPIO_PIN_RESET)
	{
	}

	/* Wait for USER push-button release before starting the Communication */
	while (BSP_PB_GetState(BUTTON_USER2) != GPIO_PIN_SET)
	{
	}

	/*##- Send IBI request ###############################################*/
	if(HAL_I3C_Tgt_IBIReq_IT(&hi3c4, ubPayloadBuffer, COUNTOF(ubPayloadBuffer)) != HAL_OK)
	{
	  /* Error_Handler() function is called when error occurs. */
	  Error_Handler();
	}

	/* Wait for IBI process ending */
	while(ubIBIcplt == 0U)
	{
	}

	/* Reset Global variable */
	ubIBIcplt = 0U;
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

